package eu.artist.postmigration.nfrvt.eval.run;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	public static final String ATT_INPUT_MODEL_PATH = "ATT_MODEL_PATH"; //$NON-NLS-1$

	// GML analysis specific attributes
	public static final String ATT_GOALMODELEVALUATION_NAME = "ATT_GOALMODELEVALUATION_NAME"; //$NON-NLS-1$
	public static final String ATT_OUTPUT_MODEL_PATH = "ATT_RESULT_PATH"; //$NON-NLS-1$
	public static final String GML_PROCESS_FACTORY_ID = "eu.artist.postmigration.nfrvt.eval.run.process.processFactory"; //$NON-NLS-1$
	
	// The plug-in ID
	public static final String PLUGIN_ID = "eu.artist.postmigration.nfrvt.eval.run"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;
	
	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

}
